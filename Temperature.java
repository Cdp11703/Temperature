public class Temperature {
    // The Program
    private double celcius;
    private double fahrenheit;

    public Temperature(){
        this.celcius = 0.0;
        this.fahrenheit = 0.0;
    }
    
    public void setCelcius(double celcius) {
        this.celcius = celcius;
    }

    public void setFahrenheit(double fahrenheit) {
        this.fahrenheit = fahrenheit;
    }
}
